La Maipú is an iconic <b>135 sqare mile</b> Estancia in the Lake San Martin área. Its
main house is where we have all our private cozy rooms. Two triple and four
double, all with private bathroom and heating.<br><br>
3 hours a day of electricity and only 2 hours of satellite wi-fi per day per person,
will help you relax and unwind. Actually, after a couple of days you&#39;ll stop
missing it.<br><br>
We also have a Refuge, our cheapest accommodation, similar to a hostel,
perfect for solo travelers, small groups or families.<br><br>
In addition to the breathtaking views, this Estancia is famous for its history. The
English brothers Lively were the first settlers in the area. Joseph Lively arrived at
Lago San Martín in 1902, thanks to Francisco &quot;Perito&quot; Moreno, who granted
them the right to land before the territorial dispute between Argentina and
Chile.<br><br>
The current owners of La Maipú preserved José&#39;s cabin as a museum. When you
visit the cabin, it will take you back to the old Patagonia, the one of the natives
and some adventurous foreigners. It will help you see and discover the history
of authentic Patagonian pioneers.<br><br>
<u>ACTIVITIES:</u><br><br>
<b>- Horseback ride.</b> You can also enjoy a horseback ride through different trails of the estancia.
Most of them were created by the shepherds, who still move on horseback to look after the
sheep.<br>
Our favorite trail is the Astillado, which takes approximately 8 hours in both ways to cross the
lenga’s forest and see this 2005-meter-high mountain.<br><br>
<b>- Trekking</b>. A paradise for those who love to get lost and explore untouched wilderness.<br>
At La Maipú we have more than 8 trails of all sorts of difficulty that will fit everyone, from kids
to serious athletes.<br><br>
<b>- Fishing.</b> We are not a fishing lodge but we all enjoy spending a few hours fishing every now
and then, whenever the weather is good enough for it.<br>
Lake San Martin is full of trouts of all kinds, grab one of our fishing kits and give it a try.<br><br>

We&#39;ll also be hosting fishing sessions for those who are completely new to this and interested
on learning more.<br><br>
<b>- Birding.</b> There are more than 40 species of birds out there, some that live all year round and
others that migrate. Including Andean Condors, who live right next to our main facility.<br><hr>
