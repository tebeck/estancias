En la margen norte del Lago San Martín, provincia de Santa Cruz, Patagonia sur argentina, sobre el
brazo Internacional, casi en el límite con Chile, se encuentra la estancia La Josefina, 17 mil
hectáreas, con 40 km de costa del lago más turquesa. <br><br>
Al no haber camino vehicular para llegar, sólo es accesible por agua o por estrechas sendas para
caminantes o caballos, La Josefina es una de las estancias menos conocidas, incluso por los
lugareños.<br><br>
Bosques de coihues, lenga y ñire. Ríos que forman decenas de lagunas. Cascadas, glaciares. Valles
prácticamente vírgenes, nieves eternas, rocas de colores, dan marco a los más emblemáticos
animales silvestres de la zona. Zorros colorado, huemules, chinchillones, pumas, cóndores, pato de
los torrentes, conviven con las vacas salvajes que colonizaron la zona a partir de la llegada del
hombre, en los finales del siglo 19.<br><br>
Inserta en un proyecto de Conservación de los bosques nativos y de su flora y fauna asociada, la
Estancia La Josefina estará abierta al turismo a partir de noviembre 2020.<br><br>
Caminatas, cabalgatas, pesca, con las rústicas instalaciones de la estancia, que cuenta con una
casa con tres dormitorios con baños, calefacción a leña, sin señal de teléfono ni internet, pero sí
con comidas típicas de la Patagonia. Asados, guisos, pan casero, truchas recién pescadas, son
algunas de las delicias que encontrarán en la Josefina.<br>
