  <div class="englishtext animated fadeIn"><br>

On the northern margin of Lake San Martin, province of Santa Cruz, southern Patagonia, on the
International arm, almost on the border with Chile, is the La Josefina farm, 17 thousand hectares,
with 40 km of turquoise lake coast. <br><br>
Since there is no vehicular road to reach, it is only accessible by water or by narrow paths for
walkers or horses, La Josefina is one of the least known Estancias.<br><br>
Coihues, lenga and ñire forests. Rivers that form dozens of lagoons. Waterfalls, glaciers. Virtually
virgin valleys, eternal snows, colored rocks, frame the most emblematic wild animals in the area.
Red foxes, huemules, chinchillones, pumas, condors, duck of torrents, coexist with the wild cows
that colonized the area from the arrival of man, in the late 19th century.<br><br>

Inserted in a project of Conservation of native forests and their associated flora and fauna,
Estancia La Josefina will be open to tourism in November 2020.<br><br>
Walks, horseback riding, fishing, with the rustic facilities of a Estancia, which has a house with
three bedrooms with bathrooms, wood heating, no telephone signal or internet, but with typical
Patagonia meals. Asados, stews, homemade bread, freshly caught trouts, are some of the delights
that you will find in the Josefina.<br>
