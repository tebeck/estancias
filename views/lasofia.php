        At the foot of  “The Andes”, in a magnificent frame of high mountains, winding rivers and wide
steppes, “Estancia La Sofía” invites tourists from all around the world to discover the magical
charm of Southern Patagonia, in Argentina.<br><br>
Estancia La Sofia offers it’s visitors 4 double suites and 2 triple rooms. All of them comfortably
and fully equipped for every guest to enjoy a delightful stay. The lodge also offers a lovely and
cosy living room.<br><br>
<u>ACTIVITIES:</u><br><br>
<b>- Horseback ride to the Viedma</b> viewpoint, where you can see the cerro Torre
and Fitz Roy. We can also view all the Viedma Lake, cerro Agassiz, Pintado and
Sombrero, also the way of the Guanaco river.<br>
Upon our return, we will eat a classic Patagonian food.<br><br>

<b>-Trekking Laguna Azul</b>, inside the Los Glaciares National Park, in one of the most
virgin places in all of Patagonia Austral. 6-7 hours of walking including a lunch.
Upon our return, we will eat a classic Patagonian food.<br><br>

<b>-Horsbeack ride to Lago Argentino</b>, we will arrive at a viewpoint where we can
see all the lake, the cerro Castillo, cerro Frías, and if the day allows it, Torres del
Paine. Then we go to the Lengas forest where several melting rivers come
together.<br>
Upon our return, we will eat a classic Patagonian food.<br><hr>
